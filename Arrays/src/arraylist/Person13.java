/*
 * User Defined Class for Example 13.3
 */

public class Person13 {

    private String name;
    private int age;

    public Person13(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public void printData() {
        System.out.println(name + " " + age);
    }
}

